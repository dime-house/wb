/**
 * Разделяет строку по запятой и удаляет пробелы вокруг каждого элемента.
 *
 * @param value - Исходная строка для разделения.
 * @returns Массив строк, разделенных запятыми и обрезанных пробелами.
 */
export function splitByComma(value: string): string[] {
	// Разделение строки по запятой и обрезка пробелов
	return value.split(',').map((piece) => piece.trim());
}
